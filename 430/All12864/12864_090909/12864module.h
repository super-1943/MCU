/************************************************************************
显示整数函数

参数说明：
--------myint:需要显示的整数
--------x_pos:显示位置x坐标（0~15）
--------y_pos:显示位置y坐标（0~6）
************************************************************************
void show_int(unsigned int myint,unsigned char x_pos,unsigned char y_pos)
{
  unsigned char t=0;
  unsigned char buffer[6]={10,10,10,10,10,10};
  while(myint/10)
  {
    buffer[5-t]=myint%10;
    myint/=10;
    t++;
  }
  buffer[5-t]=myint;
  for(t=0;t<6;t++)
  {
    if(buffer[t]==10)
    {
      continue;
    }
    else
    {
      show_shuzi(x_pos*8,y_pos,buffer[t]);
    }
    x_pos++;
    if(x_pos>128)
    {
      y_pos++;
      x_pos=0;
    }
  }
}
//向12864发送命令，参数即为命令
void send_cmd(unsigned char cmd)
{
  IsWrite;
  IsComd;  
  E_1;
  DataBus=cmd;
  E_0;  
}
//向12864发送点阵数据
void send_data(unsigned char d)
{
  IsWrite;
  IsData; 
  E_1;
  DataBus=d;
  E_0; 
}
//设置点阵数据地址
void set_pos(unsigned char x,unsigned char y)//X为列地址,Y为页地址
{
  unsigned char tmp;
  y&=0x0f;
  tmp=0xb0;
  tmp|=y;
  send_cmd(tmp);
  tmp=0x10;
  tmp|=(x>>4);//先取高四位
  send_cmd(tmp);
  x&=0x0f;
  send_cmd(x);
}
//清屏，其实就是全发送0
void clear(void)
{
  unsigned char x,y;
  for(y=0;y<8;y++)
  {
    set_pos(0,y);
    for(x=0;x<128;x++)
    {
      send_data(0x00);//列地址自动往后加
    }
  }
}
//初始化12864
void init_12864(void)
{
  DataDir=0xff;
  DataBus=0x00;
  CtrlDir=0xAA;
  delay(10);
  send_cmd(M_LCD_ON);
 send_cmd(M_LCD_POWER_ALL);  //好像必须用这个,选择其他单个的上电控制都不亮,还没有搞明白是怎么回事
                             //必须设置,否则不亮
  send_cmd(M_LCD_ELE_VOL);  //调整显示屏的亮度
  send_cmd(0x12);//0x01-0x3f
  send_cmd(M_LCD_SETR_5);//V5电压内部电阻调节设置,电阻越大分压越大,0x20-0x27
  send_cmd(M_LCD_COM_NOR);
  send_cmd(0x40);
  clear();
}
/**************************************************
设置对应坐标点
参数：x,y
x:横坐标（0~127）
y:纵坐标(0~63)
d:数据(0 or 1)
************************************************/
void set_point(unsigned char x,unsigned char y,unsigned char d)
{
  unsigned char itsdata=0x01,itsx,itsy;
  itsx=x&0x7f;
  itsy=(y&0x3f)>>3;//相当于取页
  set_pos(127-itsx,itsy);//将原点转到左上角
  itsdata<<=(y&0x07);//即在页里找到对应的行(也就是在某一页的某一列中选择一个点亮)
  if(d)             //但每一页的每一列都是一个字节，操作这一列的某一行会干扰这一列其他行
  {                 //应用时应注意这一点
    send_data(itsdata);
  }
  else
  {
    send_data(0x00);
  }
}

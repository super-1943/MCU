unsigned char fnPR1(unsigned char uCmd,unsigned char uPar1,unsigned char uPar2) // 写双参数的指令
{
	if(fnST01() == 0)
		return 1;
	LCMDW = uPar1;
	if(fnST01() == 0)
		return 2;
	LCMDW = uPar2;
	if(fnST01() == 0)
		return 3;
	LCMCW = uCmd;
	return 0; //  0返回 成功
}
unsigned char fnPR11(unsigned char uCmd,unsigned char uPar1) // 写单参数的指令
{
	if(fnST01() == 0)
		return 1;
	LCMDW = uCmd;
	if(fnST01() == 0)
		return 2;
	LCMCW = uPar1;
	return 0; //  0返回 成功
}

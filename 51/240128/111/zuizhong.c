unsigned char fnST01(void) //  STA1,STA0状态位 判断（读写指令和读写数据）
{
	unsigned char i;
	for(i=10;i>0;i--)
	{
		if((LCMCW & 0x03) == 0x03)
		break;
	}
	return i; // 若返回零，说明错误
}
